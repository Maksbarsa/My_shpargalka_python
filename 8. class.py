"–ø—Ä–∏–º–µ—Ä: @staticmethod - —ç—Ç–æ –º–µ—Ç–æ–¥ –≤–Ω—É—Ç—Ä–∏ –∫–ª–∞—Å—Å–∞, –∫–æ—Ç–æ—Ä—ã–π: –Ω–µ —Ä–∞–±–æ—Ç–∞–µ—Ç –Ω–∏ —Å –æ–±—ä–µ–∫—Ç–æ–º (self), –Ω–∏ —Å –∫–ª–∞—Å—Å–æ–º (cls)"
