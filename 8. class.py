        # (–∏–∑-–∑–∞ @staticmethod –æ–Ω –ù–ï –ø–æ–ª—É—á–∞–µ—Ç self, –ø–æ—ç—Ç–æ–º—É –ø–∏—à–µ–º –ø—Ä–æ—Å—Ç–æ:)
        print("–ü—Ä–∏–≤–µ—Ç! –Ø –æ–±—ã—á–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è.")  # –ö–æ–≥–¥–∞ –º–µ—Ç–æ–¥ hello() –±—É–¥–µ—Ç –≤—ã–∑–≤–∞–Ω, –æ–Ω –ø—Ä–æ—Å—Ç–æ –Ω–∞–ø–µ—á–∞—Ç–∞–µ—Ç —Ç–µ–∫—Å—Ç –≤ –∫–æ–Ω—Å–æ–ª—å.

