class Calc:    # –ö–ª–∞—Å—Å
    num = 100  # –ê–¢–†–ò–ë–£–¢ –ö–õ–ê–°–°–ê

                                        # –ê–¢–†–ò–ë–£–¢ –ö–õ–ê–°–°–ê:
    def __init__(self, num1 , num2):  # –∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä —ç–∫–∑–µ–º–ø–ª—è—Ä–∞ –∫–ª–∞—Å—Å–∞.
                                      # __init__ –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ —ç–∫–∑–µ–º–ø–ª—è—Ä–æ–≤ –∫–ª–∞—Å—Å–∞.
        self.num1 = num1  # –∞—Ç—Ä–∏–±—É—Ç—ã —ç–∫–∑–µ–º–ø–ª—è—Ä–∞ –∫–ª–∞—Å—Å–∞ (–∏–Ω–¥–∏–≤–∏–¥—É–∞–ª—å–Ω—ã–µ)
        self.num2 = num2  # –∞—Ç—Ä–∏–±—É—Ç—ã —ç–∫–∑–µ–º–ø–ª—è—Ä–∞ –∫–ª–∞—Å—Å–∞ (–∏–Ω–¥–∏–≤–∏–¥—É–∞–ª—å–Ω—ã–µ)

                                        # –ê–¢–†–ò–ë–£–¢ –ö–õ–ê–°–°–ê:
    def resul(self):  # –º–µ—Ç–æ–¥ —ç–∫–∑–µ–º–ø–ª—è—Ä–∞ –∫–ª–∞—Å—Å–∞.
        return self.num1 + self.num2  # –¢–µ–ª–æ –º–µ—Ç–æ–¥–∞: –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—É–º–º—É –ø–æ–ª–µ–π num1 –∏ num2 —Ç–µ–∫—É—â–µ–≥–æ –æ–±—ä–µ–∫—Ç–∞ (self).

                                        # –ê–¢–†–ò–ë–£–¢ –ö–õ–ê–°–°–ê:
    def sravn(self):  # –º–µ—Ç–æ–¥ —ç–∫–∑–µ–º–ø–ª—è—Ä–∞ –∫–ª–∞—Å—Å–∞.
        if self.num1 > self.num2:
            return self.num1
        else:
            return self.num2

                                        # –ê–¢–†–ò–ë–£–¢ –ö–õ–ê–°–°–ê:
    def sravn1(self):  # –º–µ—Ç–æ–¥ —ç–∫–∑–µ–º–ø–ª—è—Ä–∞ –∫–ª–∞—Å—Å–∞.
        if Calc.sravn(res1) > res1.num3:
            return Calc.sravn(res1)
        else:
            return res1.num3

res1 = Calc(5, 6)  # –°–æ–∑–¥–∞—ë—Ç—Å—è —ç–∫–∑–µ–º–ø–ª—è—Ä –∫–ª–∞—Å—Å–∞ res1. –í—ã–∑—ã–≤–∞–µ—Ç—Å—è Calc.__new__ ‚Üí —Å–æ–∑–¥–∞—ë—Ç—Å—è –æ–±—ä–µ–∫—Ç.
                               # –ó–∞—Ç–µ–º Python –≤—ã–∑—ã–≤–∞–µ—Ç Calc.__init__(res1, 5, 6), –∏ –≤–Ω—É—Ç—Ä–∏ res1.num1 –∏ res1.num2
                               # –ø–æ–ª—É—á–∞—é—Ç –∑–Ω–∞—á–µ–Ω–∏—è 5 –∏ 6.

res1.num3 = 500  # –°–æ–∑–¥–∞–ª–∏ –∞—Ç—Ä–∏–±—É—Ç —ç–∫–∑–µ–º–ø–ª—è—Ä–∞ –∫–ª–∞—Å—Å–∞.

print(res1.__dict__)                    # {'num1': 5, 'num2': 6}
print(Calc.resul(res1), res1.resul())   # 11 11 –¥–≤–∞ —ç—Ç–∏—Ö —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞ - —ç—Ç–æ –æ–¥–Ω–æ –∏ —Ç–æ–∂–µ
print(Calc.sravn(res1))                 # 6
print(Calc.sravn1(Calc.sravn(res1)))    # 500


"====================== –°–ø–µ—Ü–∏–∞–ª—å–Ω—ã–µ –¥–µ–∫–æ—Ä–∞—Ç–æ—Ä—ã, –∫–æ—Ç–æ—Ä—ã–µ –∏–∑–º–µ–Ω—è—é—Ç –ø–æ–≤–µ–¥–µ–Ω–∏–µ –º–µ—Ç–æ–¥–∞ –≤–Ω—É—Ç—Ä–∏ –∫–ª–∞—Å—Å–∞ ======================="

"–ø—Ä–∏–º–µ—Ä: @classmethod - —ç—Ç–æ –º–µ—Ç–æ–¥, –∫–æ—Ç–æ—Ä—ã–π —Ä–∞–±–æ—Ç–∞–µ—Ç —Å –∫–ª–∞—Å—Å–æ–º, –∞ –Ω–µ —Å –æ–±—ä–µ–∫—Ç–æ–º"
class A:
    value = 10
    @classmethod  # –ú–µ—Ç–æ–¥, –∫–æ—Ç–æ—Ä—ã–π —Ä–∞–±–æ—Ç–∞–µ—Ç —Å –ö–õ–ê–°–°–û–ú.
                  # –ü–æ–ª—É—á–∞–µ—Ç cls ‚Äî —Å—Å—ã–ª–∫—É –Ω–∞ —Å–∞–º –∫–ª–∞—Å—Å.
                  # –ú–æ–∂–µ—Ç –º–µ–Ω—è—Ç—å –∞—Ç—Ä–∏–±—É—Ç—ã –∫–ª–∞—Å—Å–∞.
                  # –ú–æ–∂–Ω–æ –≤—ã–∑—ã–≤–∞—Ç—å —á–µ—Ä–µ–∑ –æ–±—ä–µ–∫—Ç –∏–ª–∏ —á–µ—Ä–µ–∑ –∫–ª–∞—Å—Å ‚Äî –≤—Å—ë —Ä–∞–≤–Ω–æ.

    def show(cls):  # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –º–µ—Ç–æ–¥ show, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –ø–∞—Ä–∞–º–µ—Ç—Ä cls
                    # (—Ç–∞–∫ –∂–µ, –∫–∞–∫ self = –æ–±—ä–µ–∫—Ç, —Ç—É—Ç cls = –∫–ª–∞—Å—Å)

        cls.value = 5  # –í–æ—Ç —Ç–∞–∫ –º–æ–∂–Ω–æ –∏–∑–º–µ–Ω–∏—Ç—å —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–π –∞—Ç—Ä–∏–±—É—Ç –∫–ª–∞—Å—Å–∞
        cls.value1 = 3  # –í–æ—Ç —Ç–∞–∫ –º–æ–∂–Ω–æ –¥–æ–±–∞–≤–ª—è—Ç—å –∞—Ç—Ä–∏–±—É—Ç –∫–ª–∞—Å—Å–∞

        print(cls.value, cls.value1) # –¢–∞–∫ –∫–∞–∫ cls ‚Äî —ç—Ç–æ A, —Ç–æ: cls.value ‚Üí A.value ‚Üí 10

A.show()  # 5 3
obj = A()
