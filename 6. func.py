"========================================= Функция с возвращаемым значением ==========================================="

def add(a, b = 100):  # Функция с параметром по умолчанию (b = 100)
    return a + b

result1 = add(5, 3)    # Позиционные аргументы

result2 = add(b=10, a=8)     # Именованные аргументы

print(f"5 + 3 = {result1}")
# print: 5 + 3 = 8
print(f"10 + 8 = {result2}")
# print: 10 + 8 = 18


